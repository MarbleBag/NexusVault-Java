package nexusvault.format.tex;

import nexusvault.shared.exception.NexusVaultException;

public class TextureException extends NexusVaultException {

	private static final long serialVersionUID = 4589409000959753398L;

	public TextureException() {
		super();
	}

	public TextureException(String s) {
		super(s);
	}

	public TextureException(String message, Throwable cause) {
		super(message, cause);
	}

	public TextureException(Throwable cause) {
		super(cause);
	}

}
