import nexusvault.shared.exception.ExportException;

public final class OnTextureRequestException extends ExportException {

	private static final long serialVersionUID = -2047895574718079791L;

	public OnTextureRequestException() {
		super();
	}

	public OnTextureRequestException(String s) {
		super(s);
	}

	public OnTextureRequestException(String message, Throwable cause) {
		super(message, cause);
	}

	public OnTextureRequestException(Throwable cause) {
		super(cause);
	}

}
