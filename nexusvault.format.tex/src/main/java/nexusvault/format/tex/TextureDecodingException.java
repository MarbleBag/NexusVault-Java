package nexusvault.format.tex;

public final class TextureDecodingException extends TextureException {

	private static final long serialVersionUID = 1L;

	public TextureDecodingException() {
		super();
	}

	public TextureDecodingException(String s) {
		super(s);
	}

	public TextureDecodingException(String message, Throwable cause) {
		super(message, cause);
	}

	public TextureDecodingException(Throwable cause) {
		super(cause);
	}
}