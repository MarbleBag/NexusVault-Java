package nexusvault.format.tex;

public final class TextureEncodingException extends TextureException {

	private static final long serialVersionUID = 1L;

	public TextureEncodingException() {
		super();
	}

	public TextureEncodingException(String s) {
		super(s);
	}

	public TextureEncodingException(String message, Throwable cause) {
		super(message, cause);
	}

	public TextureEncodingException(Throwable cause) {
		super(cause);
	}
}