package nexusvault.format.tex;

public final class TextureDataDecoderNotFoundException extends TextureException {

	private static final long serialVersionUID = 3771128651987236198L;

	public TextureDataDecoderNotFoundException() {
		super();
	}

	public TextureDataDecoderNotFoundException(String s) {
		super(s);
	}

	public TextureDataDecoderNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public TextureDataDecoderNotFoundException(Throwable cause) {
		super(cause);
	}

}
