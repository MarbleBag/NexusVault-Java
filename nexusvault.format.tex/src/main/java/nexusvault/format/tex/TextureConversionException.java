package nexusvault.format.tex;

public final class TextureConversionException extends TextureException {

	private static final long serialVersionUID = -7484677405271655866L;

	public TextureConversionException() {
		super();
	}

	public TextureConversionException(String s) {
		super(s);
	}

	public TextureConversionException(String message, Throwable cause) {
		super(message, cause);
	}

	public TextureConversionException(Throwable cause) {
		super(cause);
	}

}
